# Импорт экземпляра приложения FastAPI из основного модуля
# app содержит все маршруты и настройки веб-приложения
from main import app

# Импорт ASGI-сервера uvicorn
# uvicorn используется для запуска FastAPI приложения
import uvicorn

# Проверка, что скрипт запущен напрямую, а не импортирован как модуль
if __name__ == "__main__":
    # Запуск веб-сервера с указанными параметрами:
    # app - экземпляр FastAPI приложения
    # host="0.0.0.0" - сервер будет доступен по всем сетевым интерфейсам
    # port=8000 - сервер будет слушать порт 8000
    uvicorn.run(app, host="0.0.0.0", port=8000)