# Импортируем BaseModel из pydantic для создания схем данных
# Pydantic используется для валидации данных и автоматической генерации документации
from pydantic import BaseModel

# Схема для регистрации нового пользователя
class UserRegisterSchema(BaseModel):
    name: str      # Имя пользователя
    email: str     # Email пользователя (будет использоваться для входа)
    password: str  # Пароль пользователя (будет хешироваться перед сохранением)

# Схема для авторизации пользователя
class UserLoginSchema(BaseModel):
    email: str     # Email пользователя для входа
    password: str  # Пароль пользователя для проверки

# Схема для отображения данных пользователя
# Используется при возврате данных пользователя (например, в списке всех пользователей)
class UserSchema(BaseModel):
    id: int        # Уникальный идентификатор пользователя
    name: str      # Имя пользователя
    email: str     # Email пользователя