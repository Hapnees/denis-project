import urllib.parse

def parse_book_name(bookName: str) -> str:
    """
    Функция подготавливает название книги для использования в URL-адресе.
    
    Что делает функция:
    1. Принимает обычное название книги (например, "Война и мир")
    2. Кодирует специальные символы и пробелы
    3. Возвращает строку, которую можно безопасно использовать в URL
    
    Аргументы:
        bookName (str): Обычное название книги, которое ввёл пользователь
            Например: "Война и мир" или "Гарри Поттер и философский камень"
            
    Возвращает:
        str: Закодированное название книги, пригодное для использования в URL
            Например: "Война+и+мир" или "Гарри+Поттер+и+философский+камень"
    """
    # Используем функцию quote_plus из модуля urllib.parse
    # Эта функция:
    # 1. Заменяет пробелы на знак '+'
    # 2. Кодирует специальные символы (например, '&' становится '%26')
    # 3. Кодирует русские буквы в формат URL
    parsed_payload = urllib.parse.quote_plus(bookName)
    
    # Возвращаем закодированную строку
    return parsed_payload